// Callbacks that pages can register for re-rendering on language change
const _langChangeCallbacks = [];

function onLangChange(cb) {
  _langChangeCallbacks.push(cb);
}

function getCurrentLang() {
  return localStorage.getItem("gs_lang") || "en";
}

function setLang(lang) {
  localStorage.setItem("gs_lang", lang);
  applyTranslations();
  // Notify all registered callbacks so dynamic content re-renders
  _langChangeCallbacks.forEach((cb) => cb(lang));
}

function t(key) {
  const lang = getCurrentLang();
  return (
    (translations[lang] && translations[lang][key]) ||
    translations.en[key] ||
    key
  );
}

function applyTranslations() {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = t(key);
    } else if (el.tagName === "OPTION") {
      el.textContent = t(key);
    } else {
      el.textContent = t(key);
    }
  });

  // Update page title with app name
  const titleEl = document.querySelector("title");
  if (titleEl) {
    const currentTitle = titleEl.textContent;
    // Keep "GramSeva" in the title but it won't be translated
  }

  // Update language selector if present
  const langSelect = document.getElementById("lang-select");
  if (langSelect) {
    langSelect.value = getCurrentLang();
  }

  // Set html lang attribute
  document.documentElement.lang = getCurrentLang();

  // Set direction for RTL languages (Urdu)
  if (getCurrentLang() === "ur") {
    document.documentElement.dir = "rtl";
  } else {
    document.documentElement.dir = "ltr";
  }
}

function renderLanguageSelector(containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;

  const currentLang = getCurrentLang();
  let html = `<select id="lang-select" class="lang-selector" onchange="setLang(this.value)" aria-label="Select language">`;
  for (const [code, name] of Object.entries(languageNames)) {
    html += `<option value="${code}" ${code === currentLang ? "selected" : ""}>${name}</option>`;
  }
  html += `</select>`;
  container.innerHTML = html;
}

// Apply on load
document.addEventListener("DOMContentLoaded", () => {
  applyTranslations();
});
